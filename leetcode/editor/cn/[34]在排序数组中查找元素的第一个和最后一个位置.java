class Solution {
    public int[] searchRange(int[] nums, int target) {
        int[] a = {-1, -1};
        int[] b = new int[2];
        List<Integer> list = new ArrayList<>();
        for (int i = 0; i < nums.length; i++) {
            if (target == nums[i]) {
                list.add(i);
            }
        }
        if (list.size() == 0) {
            return a;
        }
        b[0] = list.get(0);
        b[1] = list.get(list.size() - 1);
        return b;
    }
}
